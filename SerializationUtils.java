public class SerializationUtils {
    private static Object Categories;

    public static Object Object;

    public static java.lang.Object clone(Categories baju) {
        return baju;
    }

    public Object clone() {
        return Categories;
    }

    public Object Clone() {
        try {
            return super.clone();
        } catch (CloneNotSupportedException e) {
            return Categories;
        }

    }
}
